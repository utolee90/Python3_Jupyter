# 문자열 여러개 한꺼번에 저장
str_list = ['국어', '영어', '수학', '사회', '한국사']

print(str_list)
# 인덱싱 : 데이터 1개 처리
print(str_list[0])
print(str_list[3])
# 슬라이싱 : 데이터 여러개 처리
print(str_list[1:4])
print(str_list[:4])
print(str_list[1:])
print(str_list[:])
print(str_list[::2])
print(str_list[::-1])
print('-' * 30)

# 정수 저장
num_list = [1, 2, 3, 4, 5]
print(num_list)
print('-' * 30)

# 실수 저장
float_list = [5.5, 3.14, 7.7]
print(float_list)
print('-' * 30)

# boolean값 저장
bool_list = [True, False, False]
print(bool_list)
print('-' * 30)

# 여러 데이터 저장
list1 = [5, 7.7, True, "Hello"]
print(list1)
print('-' * 30)

















