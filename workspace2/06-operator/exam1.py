# 산술 연산자 +, -, *, /, //, %

num1 = int(input('정수입력 : '))
num2 = int(input('정수입력 : '))

result1 = num1 + num2
result2 = num1 - num2
result3 = num1 * num2
result4 = num1 / num2
result5 = num1 // num2
result6 = num1 % num2

print(result1, type(result1))
print(result2, type(result1))
print(result3, type(result1))
print(result4, type(result1))
print(result5, type(result1))
print(result6, type(result1))