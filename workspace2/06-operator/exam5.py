# 관계연산자 : 양쪽 데이터의 관계를 따져서, 참과 거짓을 구함
# 연산결과 : True, False

a = int(input('첫 번째 정수를 입력해주세요 '))
b = int(input('두 번째 정수를 입력해주세요 '))

result = a > b
print(result)
print('='*30)

result = a < b
print(result)
print('='*30)

result = a >= b
print(result)
print('='*30)

result = a <= b
print(result)
print('='*30)

result = a == b
print(result)
print('='*30)

result = a != b
print(result)
print('='*30)